			<p><b class="sh">About Me</b><br />
			My name is Aditya Mukherjee. I have a middle name too, but I don&#8217;t use it except on official documents. Yes, that&#8217;s me on the right. No, that&#8217;s just how my hair gets after a&nbsp;shower.</p>

			<p>I am none of the fancy things people on the Internet like to call themselves these days. I am not a <span class="sc">designer</span>&#8212;but I know how to make beautiful things usable and usable things beautiful. I am not a <span class="sc">developer</span>&#8212;but I have been involved in front and back end development of small-to-medium scale web services. I am not a <span class="sc">writer</span>&#8212;but I know how to write about things in a way you will enjoy reading. I am not a <span class="sc">musician</span>&#8212;but music is as much a part of me as my arms and legs. In a way, I am all of those things, and none of them. There is no <em>one</em> thing that really defines me; I am whatever I need to be to tell my story effectively. And if there&#8217;s anything I have at a given time, it&#8217;s a story to tell.</p>

			<p>I currently live in Gurgaon, India, having recently graduated from Lancaster University (MEng. Computer Systems).</p>

			<p><br />
			<b class="sh">About The Website</b><br />
			This is my home, my <em>casa</em>, my playground. After having experimented with an aggregated <em>brand</em> website, the ideas of which went on to form <a href="http://attico.us/" target="_blank">Atticous</a>, I abandoned it in favour of a new experimental blogging system which lets me write more freely and frequently.</p>

			<p>Everything is separated and filed into "ideas" or topics. Each topic has its own page, which contains everything that I've written on that topic till date. Sometimes an idea from one topic bears relation to another topic; this is shown as a relation between the two. The thicker the connecting line, the stronger the relation. It represents my way of thinking, where I'm constantly thinking about different but related things that don't necessarily need long, deliberated and drawn out essays. It's a more organised web of version of post-its, if you will.</p>

			<p>I also have a long-form section, where I write my longer pieces. These are few and far between, depending on how much time I have to write them.</p>

			<p>The nature of this website demands the latest versions of modern browsers because they support experimental CSS, HTML and JS that make the lives of designers easier. I follow standards, and I try to make sure things look presentable in latest versions of Google Chrome and Mozilla Firefox. You&#8217;re welcome to point out things that don&#8217;t look correct if you use the latest version of a modern browser (which you really should be doing in any case) though.</p>

